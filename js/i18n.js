function updateLanguage(lang) {
    document.querySelectorAll('[data-i18n]').forEach(element => {
        const key = element.getAttribute('data-i18n');
        if (translations[lang] && translations[lang][key]) {
            if (element.tagName === 'INPUT' || element.tagName === 'TEXTAREA') {
                element.placeholder = translations[lang][key];
            } else {
                element.innerHTML = translations[lang][key];
            }
        }
    });

    // Update HTML lang attribute
    document.documentElement.lang = lang;

    // Update current lang label in the dropdown
    const currentLangText = document.getElementById('current-lang-text');
    const currentLangFlag = document.getElementById('current-lang-flag');

    if (currentLangText) {
        currentLangText.textContent = lang.toUpperCase();
    }

    const flags = {
        'en': 'ðŸ‡ºðŸ‡¸',
        'fr': 'ðŸ‡«ðŸ‡·',
        'lo': 'ðŸ‡±ðŸ‡¦',
        'ja': 'ðŸ‡¯ðŸ‡µ',
        'th': 'ðŸ‡¹ðŸ‡­'
    };

    if (currentLangFlag) {
        currentLangFlag.textContent = flags[lang] || '';
    }

    // Save preference
    localStorage.setItem('preferred_language', lang);

    // Update active state of language buttons
    document.querySelectorAll('.lang-btn').forEach(btn => {
        btn.classList.toggle('active', btn.getAttribute('data-lang') === lang);
    });
}

document.addEventListener('DOMContentLoaded', () => {
    // Check for saved language or use browser default
    const savedLang = localStorage.getItem('preferred_language');
    const browserLang = navigator.language.split('-')[0];
    const defaultLang = translations[savedLang] ? savedLang : (translations[browserLang] ? browserLang : 'en');

    updateLanguage(defaultLang);

    // Add event listeners to language buttons
    document.addEventListener('click', (e) => {
        if (e.target.classList.contains('lang-btn')) {
            const lang = e.target.getAttribute('data-lang');
            updateLanguage(lang);
        }
    });
});
